#include "ft_list.h"

t_list		*ft_list_last(t_list *begin_list)
{
	t_list *a;

	a = begin_list;
	if (a)
	{
		while (a->next)
		{
			a = a->next;
		}
		return (a);
	}
	return (0);
}
